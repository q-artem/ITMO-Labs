SELECT
    "Н_ЛЮДИ"."ОТЧЕСТВО",
    "Н_ВЕДОМОСТИ"."ЧЛВК_ИД",
    "Н_СЕССИЯ"."ДАТА"
FROM
    "Н_ЛЮДИ"
        INNER JOIN
    "Н_ВЕДОМОСТИ" ON "Н_ЛЮДИ"."ИД" = "Н_ВЕДОМОСТИ"."ЧЛВК_ИД"
        INNER JOIN
    "Н_СЕССИЯ" ON "Н_ВЕДОМОСТИ"."СЭС_ИД" = "Н_СЕССИЯ"."ИД"
WHERE
    "Н_ЛЮДИ"."ФАМИЛИЯ" > 'Ёлкин'
  AND "Н_ВЕДОМОСТИ"."ИД" < 1490007;

